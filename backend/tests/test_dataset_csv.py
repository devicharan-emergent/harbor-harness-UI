# ── Import ───────────────────────────────────────────────────────────
def _make_bug_bench_csv(prefix: str, n: int = 2, missing_repo: bool = False) -> bytes:
    rows = []
    for i in range(n):
        rows.append({
            "instance_id": f"{prefix}_{i}",
            "name": f"PW Test {i}",
            "description": "auto-generated by test_dataset_csv.py",
            "problem_statement": f"Test bug {i}",
            "natural_language_tests": "<test_cases><test_case>Verify the import worked.</test_case></test_cases>",
            "repo": "" if missing_repo else "org/example-repo",
            "eph_job_id": "eph-abc123",
        })
    header = list(rows[0].keys())
    buf = io.StringIO()
    w = _csv.DictWriter(buf, fieldnames=header)
    w.writeheader()
    for r in rows:
        w.writerow(r)
    return buf.getvalue().encode("utf-8")


def _make_legacy_bug_bench_csv(prefix: str) -> bytes:
    """Old-shape CSV that still carries the now-dropped tags / agent_name /
    model_name columns. Used by `test_legacy_columns_silently_ignored`."""
    rows = [{
        "instance_id": f"{prefix}_legacy",
        "name": "PW Legacy",
        "description": "Legacy CSV with deprecated columns",
        "tags": "TEST_pw,csv",            # ignored
        "agent_name": "should-be-dropped", # ignored
        "model_name": "should-be-dropped", # ignored
        "problem_statement": "Legacy import test",
        "natural_language_tests": "<test_cases><test_case>It still imports.</test_case></test_cases>",
        "repo": "org/example-repo",
        "eph_job_id": "eph-legacy-1",
    }]
    header = list(rows[0].keys())
    buf = io.StringIO()
    w = _csv.DictWriter(buf, fieldnames=header)
    w.writeheader()
    for r in rows:
        w.writerow(r)
    return buf.getvalue().encode("utf-8")


class TestDatasetCsvImportExport:
    @pytest.fixture(scope="class")
    def prefix(self):
        return f"TEST_pw_csv_{int(time.time())}"

    def test_01_import_creates_rows(self, prefix):
        csv_bytes = _make_bug_bench_csv(prefix, n=2)
        files = {"files": (f"{prefix}.csv", csv_bytes, "text/csv")}
        r = requests.post(
            f"{BASE_URL}/api/eval/datasets/import",
            params={"dataset_type": "bug_bench"},
            files=files,
            headers=HEADERS,
            timeout=60,
        )
        assert r.status_code == 200, r.text
        body = r.json()
        assert isinstance(body.get("created"), list), body
        assert len(body["created"]) == 2, body
        assert len(body.get("skipped", [])) == 0, body
        assert len(body.get("errors", [])) == 0, body

    def test_02_reimport_same_rows_skipped(self, prefix):
        csv_bytes = _make_bug_bench_csv(prefix, n=2)
        files = {"files": (f"{prefix}.csv", csv_bytes, "text/csv")}
        r = requests.post(
            f"{BASE_URL}/api/eval/datasets/import",
            params={"dataset_type": "bug_bench"},
            files=files,
            headers=HEADERS,
            timeout=60,
        )
        assert r.status_code == 200, r.text
        body = r.json()
        assert len(body.get("created", [])) == 0, body
        assert len(body.get("skipped", [])) == 2, body
        assert len(body.get("errors", [])) == 0, body

    def test_03_import_bad_row_reports_error(self, prefix):
        # row missing required `repo` for bug_bench
        bad_prefix = f"{prefix}_bad"
        csv_bytes = _make_bug_bench_csv(bad_prefix, n=1, missing_repo=True)
        files = {"files": ("bad.csv", csv_bytes, "text/csv")}
        r = requests.post(
            f"{BASE_URL}/api/eval/datasets/import",
            params={"dataset_type": "bug_bench"},
            files=files,
            headers=HEADERS,
            timeout=60,
        )
        # The endpoint returns 200 with per-row error in body (not 4xx)
        if r.status_code == 200:
            body = r.json()
            assert len(body.get("errors", [])) >= 1, body
            err = body["errors"][0]
            assert "index" in err
            assert "error" in err
        else:
            # If the upstream rejects with 4xx, still acceptable as long as msg references repo
            assert r.status_code in (400, 422), r.text

    def test_04_import_unknown_type_400(self, prefix):
        csv_bytes = b"instance_id\nfoo\n"
        files = {"files": ("foo.csv", csv_bytes, "text/csv")}
        r = requests.post(
            f"{BASE_URL}/api/eval/datasets/import",
            params={"dataset_type": "not_a_type"},
            files=files,
            headers=HEADERS,
            timeout=30,
        )
        assert r.status_code == 400

    def test_05_export_specific_instance_ids(self, prefix):
        params = [("dataset_type", "bug_bench")] + [
            ("instance_id", f"{prefix}_0"), ("instance_id", f"{prefix}_1"),
        ]
        r = requests.get(
            f"{BASE_URL}/api/eval/datasets/export",
            params=params,
            headers=HEADERS,
            timeout=60,
        )
        assert r.status_code == 200, r.text
        assert r.headers["content-type"].startswith("text/csv"), r.headers
        cd = r.headers.get("content-disposition", "")
        assert "filename=" in cd
        # Parse the CSV body
        text = r.text
        reader = _csv.DictReader(io.StringIO(text))
        rows = list(reader)
        assert len(rows) == 2, f"expected 2 rows, got {len(rows)}"
        iids = sorted(r_["instance_id"] for r_ in rows)
        assert iids == sorted([f"{prefix}_0", f"{prefix}_1"]), iids
        # required column for bug_bench should be present in the export
        assert "repo" in (reader.fieldnames or [])

    def test_06_export_unknown_type_400(self):
        r = requests.get(
            f"{BASE_URL}/api/eval/datasets/export",
            params={"dataset_type": "no_type"},
            headers=HEADERS,
            timeout=15,
        )
        assert r.status_code == 400

    def test_07_export_unknown_instance_404(self, prefix):
        r = requests.get(
            f"{BASE_URL}/api/eval/datasets/export",
            params={"dataset_type": "bug_bench", "instance_id": "TEST_does_not_exist_xyz"},
            headers=HEADERS,
            timeout=30,
        )
        # NOTE: upstream harness returns 500 (not 404) for missing instances;
        # BFF currently propagates it. Accept either as 'error' for this test.
        assert r.status_code in (404, 500), r.text

    def test_08_legacy_columns_silently_ignored(self, prefix):
        """Old CSVs exported before the trim still carry `tags` /
        `agent_name` / `model_name`. Import must succeed (those columns
        are dropped, not pushed into `attributes`).
        """
        legacy_prefix = f"{prefix}_legacy_drop"
        csv_bytes = _make_legacy_bug_bench_csv(legacy_prefix)
        files = {"files": ("legacy.csv", csv_bytes, "text/csv")}
        r = requests.post(
            f"{BASE_URL}/api/eval/datasets/import",
            params={"dataset_type": "bug_bench"},
            files=files,
            headers=HEADERS,
            timeout=60,
        )
        assert r.status_code == 200, r.text
        body = r.json()
        # Row should land cleanly (no errors).
        assert len(body.get("errors", [])) == 0, body
        assert (
            len(body.get("created", [])) + len(body.get("skipped", []))
        ) == 1, body
        # If created, double-check via export that `tags` / `agent_name` /
        # `model_name` did NOT leak into stored attributes.
        if body.get("created"):
            params = [("dataset_type", "bug_bench"), ("instance_id", f"{legacy_prefix}_legacy")]
            r2 = requests.get(
                f"{BASE_URL}/api/eval/datasets/export",
                params=params,
                headers=HEADERS,
                timeout=30,
            )
            assert r2.status_code == 200, r2.text
            header_line = r2.text.splitlines()[0]
            assert "tags" not in header_line.split(","), header_line
            assert "agent_name" not in header_line.split(","), header_line
            assert "model_name" not in header_line.split(","), header_line


